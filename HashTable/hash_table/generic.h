#ifndef HASH_TABLE_H
#define HASH_TABLE_H

/*
===============================================================================
ТЗ: HashTable — универсальная хэш-таблица с открытой адресацией.
-------------------------------------------------------------------------------
Основная идея:
- Хэш-таблица хранит пары "ключ–значение" в виде последовательности слотов.
- Каждый слот может находиться в одном из состояний:
  SLOT_EMPTY, SLOT_OCCUPIED или SLOT_DELETED.
- Для разрешения коллизий используется квадратичное пробирование.
- Хэш-таблица автоматически перераспределяется (rehash), если заполненность
  превышает допустимый уровень (например, 50%).

Связь с другими структурами:
- Таблица использует ранее реализованный Vector для хранения слотов.
- Каждый элемент вектора представляет собой слот: 
  [флаг состояния (1 байт)] + [ключ (key_size байт)] + [значение (val_size байт)].

Память:
- Все данные (ключи и значения) хранятся внутри выделенного блока памяти.
- При увеличении размера таблицы выполняется копирование всех элементов
  в новую таблицу с большей capacity.
===============================================================================
*/

#include <stddef.h>
#include "../../lab3/vector/generic.h"
#include "../../lab3/comparators.h"

#define SLOT_EMPTY 0   // Слот пуст, никогда не использовался
#define SLOT_OCCUPIED 1 // Слот содержит действительную пару ключ–значение
#define SLOT_DELETED 2  // Слот был занят, но элемент удалён 

/*
===============================================================================
Структура: HashTable
Назначение: Представляет универсальную хэш-таблицу с открытой адресацией.

Поля:
- values:   указатель на Vector, содержащий слоты таблицы.
- key_size: размер ключа (в байтах).
- val_size: размер значения (в байтах).
- size:     текущее количество занятых элементов.
- capacity: общее количество слотов (ёмкость таблицы).

Примечания:
- Каждый слот хранится в виде бинарного блока: [<определить структуру>].
- Размер одного слота = <подумайте хех> байт.
===============================================================================
*/
typedef struct HashTable
{
    Vector *values;   // Вектор слотов 
    size_t key_size;  // Размер ключа
    size_t val_size;  // Размер значения
    size_t size;      // Количество занятых слотов
    size_t capacity;  // Ёмкость таблицы
} HashTable;

/*
===============================================================================
Типы функций:
HashFunc — функция вычисления хэша для ключа.
CmpFunc  — функция сравнения двух ключей.

Примечания:
- Пользователь может передавать свои функции (например, для строк, чисел и т.д.).
===============================================================================
*/
//typedef int (*HashFunc)(const void *key);
typedef int (*HashFunc)(const void *key);
typedef int (*CmpFunc)(const void *a, const void *b);

/*
===============================================================================
Функция: createHashTable
Назначение: Создаёт пустую хэш-таблицу для хранения ключей и значений
            произвольного типа.

Аргументы:
- key_size: размер ключа в байтах.
- val_size: размер значения в байтах.

Возвращает:
- Указатель на новую таблицу при успешном создании.
- NULL при ошибке выделения памяти.

Начальное состояние:
- size = 0
- capacity = TABLE_MIN_SIZE
- values = вектор из TABLE_MIN_SIZE пустых слотов.
===============================================================================
*/
HashTable *createHashTable(size_t key_size, size_t val_size);
Vector **get_bucket_ptr(HashTable *table, size_t index);
/*
===============================================================================
Функция: setItemHashTable
Назначение: Добавляет или обновляет элемент в таблице.

Аргументы:
- table: указатель на таблицу.
- key: указатель на ключ.
- data: указатель на значение.
- hash: функция вычисления хэша для ключа.
- cmp: функция сравнения ключей.

Поведение:
- Если ключ уже существует, значение обновляется.
- Если ключ новый — вставляется в первый подходящий слот.
- При превышении порога заполненности (обычно >50%) вызывается rehash.

Сложность:
- В среднем O(1), в худшем случае O(n) при большом количестве коллизий.
===============================================================================
*/
void setItemHashTable(HashTable *table, void *key, void *data,
                      HashFunc hash, CmpFunc cmp);

/*
===============================================================================
Функция: getItemHashTable
Назначение: Получает указатель на значение по заданному ключу.

Аргументы:
- table: указатель на таблицу.
- key: указатель на ключ.
- hash: функция вычисления хэша.
- cmp: функция сравнения ключей.

Возвращает:
- Указатель на значение внутри таблицы.
- NULL, если элемент не найден.

Предупреждение:
- Возвращаемый указатель становится невалидным после rehash или удаления.
===============================================================================
*/
void *getItemHashTable(HashTable *table, void *key,
                       HashFunc hash, CmpFunc cmp);

/*
===============================================================================
Функция: popItemHashTable
Назначение: Удаляет элемент по ключу и возвращает копию его значения.

Аргументы:
- table: указатель на таблицу.
- key: указатель на ключ.
- hash, cmp: функции для вычисления хэша и сравнения.

Возвращает:
- Указатель на копию значения (нужно освободить вручную).
- NULL, если элемент не найден.

Поведение:
- Помечает слот как удалённый.
- Не изменяет размер capacity.
===============================================================================
*/
void *popItemHashTable(HashTable *table, void *key,
                       HashFunc hash, CmpFunc cmp);

/*
===============================================================================
Функция: getCollisionCount
Назначение: Подсчитывает количество элементов, находящихся не в своём
            первичном хэш-слоте (т.е. коллизии).

Аргументы:
- table: указатель на таблицу.
- hash: функция хэширования.

Возвращает:
- Количество элементов, вставленных не в "идеальную" позицию.

Применение:
- Используется для анализа эффективности хэш-функции.
===============================================================================
*/
unsigned long int getCollisionCount(HashTable *table, HashFunc hash);

/*
===============================================================================
Функция: freeHashTable
Назначение: Освобождает всю память, занятую таблицей.

Аргументы:
- table: указатель на хэш-таблицу (может быть NULL).

Поведение:
- Освобождает все слоты (через vectorFree).
- Освобождает саму структуру HashTable.
===============================================================================
*/
void freeHashTable(HashTable *table);

/*
===============================================================================
Функция: rehashHashTable
Назначение: Перераспределяет таблицу в новый массив большей ёмкости.

Аргументы:
- table: указатель на хэш-таблицу.
- hash, cmp: функции для перерасчёта хэшей и вставки элементов.

Поведение:
- Создаёт новый вектор с увеличенной capacity.
- Повторно вставляет все существующие элементы.
- Удаляет старый вектор.

Назначение:
- Внутренняя функция, но может использоваться для тестирования.
===============================================================================
*/
void rehashHashTable(HashTable *table, HashFunc hash, CmpFunc cmp);

/*
===============================================================================
Встроенные функции хэширования и сравнения
Назначение: Универсальные реализации для базовых типов данных.

HashInt:    возвращает хэш по значению int.
HashString: возвращает хэш для C-строки (алгоритм типа djb2).
===============================================================================
*/
int HashInt(const void *key);
int HashString(const void *key);
int HashLL(const void *key);
int HashL(const void *key);

/*
===============================================================================
Константа: TABLE_MIN_SIZE
Назначение: Минимальное количество слотов при создании таблицы.

Рекомендация:
- Нужно подумать какое число оптимально взять тут и почему
===============================================================================
*/
#define TABLE_MIN_SIZE 10

#endif // HASH_TABLE_H

